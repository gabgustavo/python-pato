import math
# https://docs.python.org/3/library/math.html

# Funciones matemáticas básicas en Python
# Redondeo de números mas cercanos
print(round(1.3))
print(round(1.7))
print('_' * 10)

# Valor absoluto (siempre positivo)
print(abs(-70))
print(abs(50))
print('_' * 10)


print(math.ceil(4.1))  # Redondeo hacia arriba
print(math.floor(4.99999))  # Redondeo hacia abajo
# print(math.isnan("15"))  # Verifica si no es un número
print(math.isnan(15))
print(math.pow(10, 3))  # Potencia
print(math.sqrt(10))  # Raíz cuadrada
