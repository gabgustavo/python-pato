nombreCurso = "Python para principiantes:: "
descripcionCurso = """Este curso de Python está diseñado para principiantes.
Cubre los conceptos básicos del lenguaje de programación Python,
incluyendo sintaxis, estructuras de datos y programación orientada a objetos."""

print(nombreCurso, descripcionCurso)
print(len(nombreCurso))  # Longitud de la cadena
print(nombreCurso[0])  # Primer caracter
print(nombreCurso[0:6])  # Subcadena desde el índice 0 hasta el 5
print(nombreCurso[6:])  # Subcadena desde el índice 6 hasta el final
print(nombreCurso[:6])  # Subcadena desde el inicio hasta el índice 5
print(nombreCurso[:])  # Cadena completa (python asume el inicio y el final)
