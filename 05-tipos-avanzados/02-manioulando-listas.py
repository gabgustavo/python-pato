mascotas = ["perro", "gato", "pez", "loro"]
print(mascotas)
print('---')

print(mascotas[3])  # Acceder al cuarto elemento

mascotas[2] = "hamster"  # Modificar el tercer elemento
print('---')
print(mascotas)
print('---')

print(mascotas[0:2])  # Acceder a una porción de la lista
print('---')

print(mascotas[0-1])  # Acceder al último elemento //-2, -3...
print('---')

print(mascotas[::2])  # Longitud de la lista con paso 2 (impares)
print('---?')
print(mascotas[1::2])  # Longitud de la lista con paso 2 (pares)
print('---?')
